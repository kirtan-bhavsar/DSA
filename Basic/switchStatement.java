package Test;

public class switchStatement {

    public static void main(String args[]) {

        int a = 10;

        switch (a) {
            case 1:
                System.out.println("a is 1");
                break;
            case 2:
                System.out.println("a is 2");
                break;
            case 3:
                System.out.println("a is 3");
                break;
            case 4:
                System.out.println("a is 4");
                break;
            default:
                System.out.println("a is undefined");
        }

    }

}
